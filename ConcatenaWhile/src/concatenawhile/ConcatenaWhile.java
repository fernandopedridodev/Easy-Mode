public class ConcatenaWhile {
    public static void main(String[] args) {
        int i = 1; // Inicializamos o contador
        
        // Bucle do-while que imprime os números do 1 ao 10
        do {
            System.out.println(i);
            i++; // Incrementamos o contador
        } while (i <= 10); // Condición do bucle
    }
}